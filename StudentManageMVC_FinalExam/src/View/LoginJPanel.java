    private void btnLoginActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnLoginActionPerformed
        String username = txtUsername.getText();
        String password = new String (txtPassword.getPassword());
        
        StringBuilder sb = new StringBuilder();
        
        if (username.equals("")){
            sb.append("Username is empty \n");
        }
        if (password.equals("")){
            sb.append("Password is empty \n");
        }
        if (sb.length()>0){
            JOptionPane.showMessageDialog(this, sb.toString(), "Invalidation",
                    JOptionPane.ERROR_MESSAGE);
            return;
        }
        if (username.equals("npt") && password.equals("123")){
            JOptionPane.showMessageDialog(this, "Login successfully");
            MainJFrame m = new MainJFrame();
            this.hide();
            m.setVisible(true);
            dispose();
        }else{
            JOptionPane.showMessageDialog(this, "Invalid username or password", "Failure",
                    JOptionPane.ERROR_MESSAGE);
        }
       
    }//GEN-LAST:event_btnLoginActionPerformed

